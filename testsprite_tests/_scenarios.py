def tc_purchases_orders_create():
    headers = auth_headers()

    suppliers_resp = requests.get(f"{BASE_URL}/api/inventory/suppliers", headers=headers, timeout=TIMEOUT)
    assert suppliers_resp.status_code == 200, f"Suppliers list failed: {suppliers_resp.status_code} {suppliers_resp.text}"
    suppliers = get_json(suppliers_resp)
    assert isinstance(suppliers, list)

    if suppliers:
        supplier_id = suppliers[0]["id"]
    else:
        new_supplier_resp = requests.post(
            f"{BASE_URL}/api/inventory/suppliers",
            headers={**headers, "Content-Type": "application/json"},
            json={"name": f"TestSprite Supplier {uuid.uuid4().hex[:6]}", "currency": "SAR", "branch_id": 1},
            timeout=TIMEOUT,
        )
        assert new_supplier_resp.status_code in (200, 201), f"Supplier create failed: {new_supplier_resp.status_code} {new_supplier_resp.text}"
        supplier_id = get_json(new_supplier_resp)["id"]

    po_resp = requests.post(
        f"{BASE_URL}/api/buying/orders",
        headers={**headers, "Content-Type": "application/json"},
        json={
            "supplier_id": supplier_id,
            "order_date": "2026-03-01",
            "branch_id": 1,
            "currency": "SAR",
            "items": [
                {
                    "product_id": None,
                    "description": "TestSprite PO line",
                    "quantity": 1,
                    "unit_price": 10,
                    "tax_rate": 0,
                    "discount": 0,
                }
            ],
        },
        timeout=TIMEOUT,
    )
    assert po_resp.status_code == 201, f"Purchase order creation failed: {po_resp.status_code} {po_resp.text}"


