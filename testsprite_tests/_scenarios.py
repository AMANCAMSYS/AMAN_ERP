def tc_treasury_transfer_create():
    headers = auth_headers()
    accounts_response = requests.get(f"{BASE_URL}/api/treasury/accounts", headers=headers, timeout=TIMEOUT)
    assert accounts_response.status_code == 200, f"Accounts list failed: {accounts_response.status_code} {accounts_response.text}"
    accounts = get_json(accounts_response)
    assert isinstance(accounts, list)

    if len(accounts) < 2:
        return

    src_id = accounts[0]["id"]
    dst_id = accounts[1]["id"]
    if src_id == dst_id:
        return

    response = requests.post(
        f"{BASE_URL}/api/treasury/transactions/transfer",
        headers={**headers, "Content-Type": "application/json"},
        json={
            "transaction_date": "2026-03-01",
            "transaction_type": "transfer",
            "amount": 1.0,
            "treasury_id": src_id,
            "target_treasury_id": dst_id,
            "description": "TestSprite transfer test",
            "exchange_rate": 1.0,
        },
        timeout=TIMEOUT,
    )
    assert response.status_code == 201, f"Transfer failed: {response.status_code} {response.text}"


def tc_treasury_transfer_reject_same_account():
    headers = auth_headers()
    accounts_response = requests.get(f"{BASE_URL}/api/treasury/accounts", headers=headers, timeout=TIMEOUT)
    assert accounts_response.status_code == 200, f"Accounts list failed: {accounts_response.status_code} {accounts_response.text}"
    accounts = get_json(accounts_response)
    assert isinstance(accounts, list)
    if not accounts:
        return

    acc_id = accounts[0]["id"]
    response = requests.post(
        f"{BASE_URL}/api/treasury/transactions/transfer",
        headers={**headers, "Content-Type": "application/json"},
        json={
            "transaction_date": "2026-01-15",
            "transaction_type": "transfer",
            "amount": 1.0,
            "treasury_id": acc_id,
            "target_treasury_id": acc_id,
            "description": "Invalid self transfer",
            "exchange_rate": 1.0,
        },
        timeout=TIMEOUT,
    )
    assert response.status_code in (400, 422), f"Expected 400/422, got {response.status_code}: {response.text}"


